// Referencias a elementos
const menu = document.getElementById("mainMenu");
const content = document.getElementById("content");
const dynamicContent = document.getElementById("dynamicContent");
const backButton = document.getElementById("backButton");

// Mostrar tema al hacer clic en un botón
document.querySelectorAll(".menu-item").forEach((button) => {
  button.addEventListener("click", () => {
    const page = button.getAttribute("data-page");
    const tema = temas[page];
    
    // Mostrar contenido dinámico
    dynamicContent.innerHTML = `
      <h1>${tema.title}</h1>
      ${tema.description}
      <button id="videoButton">Video</button>
      <button id="imageButton">Vestimenta</button>
      <button id="activityButton">Actividad interactiva</button>
      <div id="extraContent"></div>
    `;

    // Mostrar la sección de contenido y ocultar el menú
    menu.style.display = "none";
    content.style.display = "block";

    // Agregar eventos a los botones
    document.getElementById("videoButton").addEventListener("click", () => {
      document.getElementById("extraContent").innerHTML = tema.video;
    });

    document.getElementById("imageButton").addEventListener("click", () => {
      document.getElementById("extraContent").innerHTML = `
        ${tema.image}
        <h2>Vestimenta femenina</h2>
        ${tema.womenOutfit}
        <h2>Vestimenta masculina</h2>
        ${tema.menOutfit}
      `;
    });

    document.getElementById("activityButton").addEventListener("click", () => {
      document.getElementById("extraContent").innerHTML = tema.Actividad;
    });
  });
});

// Volver al menú principal
backButton.addEventListener("click", () => {
  dynamicContent.innerHTML = '';
  content.style.display = "none";
  menu.style.display = "block";
});

// Funcionalidad de cerrar sesión
document.getElementById('logoutButton').addEventListener('click', () => {
  const confirmLogout = confirm('¿Estás seguro de que deseas cerrar sesión?');
  if (confirmLogout) {
    window.location.href = 'index.html'; // Cambia a la ruta de tu archivo de inicio de sesión
  }
});